def solution(k):

    #      2^2   +   4^2   +   6^2   + ... + (k)^2
    #      1^2   +   3^2   +   5^2   + ... + (k - 1)^2
    #  - ________________________________________________
    #    (1 + 2) + (3 + 4) + (5 + 6) + ... + (k - 1 + k)
    # 
    #                2^2   +   4^2   + ... + (k - 1)^2
    #      1^2   +   3^2   +   5^2   + ... + (k)^2
    #  - ________________________________________________
    #       -1   - (2 + 3) - (4 + 5) - ... - (k - 1 + k)
    return (-1)**k * k * (k + 1)/2
